def clear_existing_questions():
    """Clear existing questions from the system"""
    headers = {"X-Admin-Key": ADMIN_API_KEY}
    
    try:
        response = requests.delete(f"{API_BASE_URL}/admin/questions/clear", headers=headers)
        if response.status_code == 200:
            print("‚úÖ Successfully cleared all existing questions")
        else:
            print(f"‚ö†Ô∏è Failed to clear questions: {response.status_code}")
            
    except requests.RequestException as e:
        print(f"‚ö†Ô∏è Error clearing questions: {e}")

def upload_questions(questions: List[Dict[str, Any]]) -> bool:
    """Upload questions to the API using the import endpoint"""
    headers = {
        "Content-Type": "application/json",
        "X-Admin-Key": ADMIN_API_KEY
    }
    
    try:
        # Use the import endpoint which expects a list of questions
        response = requests.post(
            f"{API_BASE_URL}/admin/questions/import",
            json=questions,
            headers=headers
        )
        
        if response.status_code == 200:
            print(f"‚úÖ Successfully imported all {len(questions)} questions")
            return True
        else:
            print(f"‚ùå Failed to import questions: {response.status_code} - {response.text}")
            return False
            
    except requests.RequestException as e:
        print(f"‚ùå Error uploading questions: {e}")
        return False

